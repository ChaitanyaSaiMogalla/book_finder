function copyCode1() {
    const code = document.querySelector('#contentCopy1 pre').innerText.trim();
    document.getElementById('copy1').textContent = "Copied"
    setTimeout(() => {
      document.getElementById('copy1').textContent = "Copy"; 
    }, 1500);
    navigator.clipboard.writeText(code);
}

